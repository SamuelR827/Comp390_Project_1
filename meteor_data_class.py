class MeteorDataEntry:
    def __init__(self, name, ident, nametype, recclass, mass, fall, year, reclat, reclong, geolocation, states, counties):
        self.field_name = name
        self.field_ident = ident
        self.field_nametype = nametype
        self.field_recclass = recclass
        self.field_mass = mass
        self.field_fall = fall
        self.field_year = year
        self.field_reclat = reclat
        self.field_reclong = reclong
        self.field_geolocation = geolocation
        self.field_states = states
        self.field_counties = counties
